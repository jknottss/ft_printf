#include "ft_printf.h"

void	ft_putstr(char *str, int *ptr)
{
	int		i;

	if (str == NULL)
	{
		ft_putstr("(null)", ptr);
		return ;
	}
	i = -1;
	while (str[++i])
	{
		ft_putchar(str[i], ptr);
	}
}

#include "ft_printf.h"

void	ft_putnbr(int nb, int *ptr)
{
	if (nb == -2147483648)
	{
		ft_putchar('-', ptr);
		ft_putchar('2', ptr);
		nb = 147483648;
	}
	if (nb < 0)
	{
		ft_putchar('-', ptr);
		nb = -nb;
	}
	if (nb > 9)
		ft_putnbr(nb / 10, ptr);
	ft_putchar((nb % 10) + '0', ptr);
}
